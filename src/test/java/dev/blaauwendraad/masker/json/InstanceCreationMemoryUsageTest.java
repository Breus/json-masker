    private static final ObjectMapper objectMapper = new ObjectMapper();

    @Test
    public void defaultInstanceCreation() throws IOException {
        URL targetKeyFileUrl = RandomJsonGenerator.class.getResource("/target_keys.json");
        if (targetKeyFileUrl == null) {
            throw new IllegalStateException("target_keys.json is not found.");
        }
        Set<String> targetKeys = new HashSet<>();
        objectMapper.readValue(targetKeyFileUrl.openStream(), ArrayNode.class).forEach(t -> targetKeys.add(t.textValue()));

        long memoryBeforeInstanceCreationKb = getCurrentRetainedMemory();

        JsonMasker masker = JsonMasker.getMasker(targetKeys);
        masker.mask("{\"maskMe\": \"secret\"}"); // just run once to make sure there's no side effects

        long memoryAfterInstanceCreation = getCurrentRetainedMemory();

        long memoryLimitKb = 400;
        long memoryConsumedKb = bytesToKb(memoryAfterInstanceCreation - memoryBeforeInstanceCreationKb);

        Assertions.assertThat(memoryConsumedKb)
                .withFailMessage("Expected to use less than %sKB of memory, got %sKB", memoryLimitKb, memoryConsumedKb)
                .isLessThan(memoryLimitKb);
    }

    @Test
    void realWorldObfuscatedKeysInstanceCreation() {
        long memoryBeforeInstanceCreation = getCurrentRetainedMemory();

        JsonMaskingConfig config = JsonMaskingConfig.builder()
                .maskKeys(obfuscatedTargetKeys, KeyMaskingConfig.builder().build())
                .maskJsonPaths(obfuscatedTargetJsonPaths, KeyMaskingConfig.builder().build())
                .build();
        JsonMasker masker = JsonMasker.getMasker(config);
        masker.mask("{\"maskMe\": \"secret\"}"); // just run once to make sure there's no side effects

        long memoryBeforeInstanceCreationKb = getCurrentRetainedMemory();

        long memoryLimitKb = 700;
        long memoryConsumedKb = bytesToKb(memoryBeforeInstanceCreationKb - memoryBeforeInstanceCreation);

        Assertions.assertThat(memoryConsumedKb)
                .withFailMessage("Expected to use less than %sKB of memory, got %sKB", memoryLimitKb, memoryConsumedKb)
                .isLessThan(memoryLimitKb);
    }

    private long getCurrentRetainedMemory() {
        Runtime.getRuntime().gc(); // run garbage collector
        return Runtime.getRuntime().totalMemory() - Runtime.getRuntime().freeMemory();
    }

    private long bytesToKb(long bytes) {
        return bytes / 1024;
    }
